static struct
{
    float x, y;                 /* position of happyface */
    float xvel, yvel;           /* velocity of happyface */
} faces[NUM_HAPPY_FACES];


int
randomInt(int min, int max)
{
    return min + rand() % (max - min + 1);
}

/*
	Produces a random float x, min <= x <= max
	following a uniform distribution
 */
float
randomFloat(float min, float max)
{
    return rand() / (float) RAND_MAX *(max - min) + min;
}

void initializeHappyFaces()
{
    int i;
    for (i = 0; i < NUM_HAPPY_FACES; i++) {
        faces[i].x = randomFloat(0.0f, SCREEN_WIDTH - HAPPY_FACE_SIZE);
        faces[i].y = randomFloat(0.0f, SCREEN_HEIGHT - HAPPY_FACE_SIZE);
        faces[i].xvel = randomFloat(-0.4f, 0.4f);
        faces[i].yvel = randomFloat(-0.4f, 0.4f);
    }
}

#include <fb/fb.h>

void game(void)
{
	struct fb * fb;
	struct surface_t * screen;
	struct surface_t * obj;
    struct rect_t dstRect, srcRect;
	u32_t c;
	int i;
	u32_t startFrame, endFrame;
	u32_t delay;

	initializeHappyFaces();

	fb = search_framebuffer("fb");
	screen = &fb->info->surface;

	c = surface_map_color(screen, get_color_by_name("red"));
	obj = surface_alloc_from_gimage(&image_test);

	/* setup boundaries for happyface bouncing */
    u32_t maxx = SCREEN_WIDTH - HAPPY_FACE_SIZE;
    u32_t maxy = SCREEN_HEIGHT - HAPPY_FACE_SIZE;
    u32_t minx = 0;
    u32_t miny = 0;

    /* setup rects for drawing */
    srcRect.x = 0;
    srcRect.y = 0;
    srcRect.w = HAPPY_FACE_SIZE;
    srcRect.h = HAPPY_FACE_SIZE;
    dstRect.w = HAPPY_FACE_SIZE;
    dstRect.h = HAPPY_FACE_SIZE;

    int loop = 10;
    while (loop--)
	{
    	startFrame = jiffies;

		fb->swap(fb);
		surface_fill(screen, &screen->clip, c, BLEND_MODE_REPLACE);
		//memset(screen->pixels, 0, screen->h * screen->pitch);
		for (i = 0; i < NUM_HAPPY_FACES; i++)
		{
			faces[i].x += faces[i].xvel * MILLESECONDS_PER_FRAME;
			faces[i].y += faces[i].yvel * MILLESECONDS_PER_FRAME;
			if (faces[i].x > maxx)
			{
				faces[i].x = maxx;
				faces[i].xvel = -faces[i].xvel;
			}
			else if (faces[i].y > maxy)
			{
				faces[i].y = maxy;
				faces[i].yvel = -faces[i].yvel;
			}
			if (faces[i].x < minx)
			{
				faces[i].x = minx;
				faces[i].xvel = -faces[i].xvel;
			}
			else if (faces[i].y < miny)
			{
				faces[i].y = miny;
				faces[i].yvel = -faces[i].yvel;
			}
			dstRect.x = faces[i].x;
			dstRect.y = faces[i].y;
			surface_blit(screen, &dstRect, obj, &srcRect, BLEND_MODE_ALPHA);
		}
		fb->flush(fb);

		endFrame = jiffies;

		delay = MILLESECONDS_PER_FRAME - (endFrame - startFrame) * 10;
		if (delay < 0)
			delay = 0;
		else if (delay > MILLESECONDS_PER_FRAME)
			delay = MILLESECONDS_PER_FRAME;
		mdelay(delay);
	}

    surface_free(obj);
}

inline bool_t PixelsIsInPic(struct surface_t * src,const long x,const long y)
{
    return ( (x>=0)&&(x<src->w) && (y>=0)&&(y<src->h) );
}

void PicRotary0(struct surface_t * Dst, struct surface_t * Src,double RotaryAngle,double ZoomX,double ZoomY,double move_x,double move_y)
{
    double rx0 = Src->w * 0.1;
    double ry0 = Src->h * 0.5;
    long x,y;

    for (y=0;y<Dst->h;++y)
    {
        for (x=0;x<Dst->w;++x)
        {
            long srcx=(long)((x- (move_x+rx0))/ZoomX*cos(RotaryAngle) - (y- (move_y+ry0))/ZoomY*sin(RotaryAngle) + rx0) ;
            long srcy=(long)((x- (move_x+rx0))/ZoomX*sin(RotaryAngle) + (y- (move_y+ry0))/ZoomY*cos(RotaryAngle) + ry0) ;

            if(PixelsIsInPic(Src, srcx, srcy))
            	surface_sw_set_pixel(Dst, x, y, surface_sw_get_pixel(Src, srcx, srcy));
        }
    }
}

