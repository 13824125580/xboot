#define REALVIEW_CLCD_OFFSET_LBAS		(0x014)
#define REALVIEW_CLCD_OFFSET_CNTL		(0x018)
#define REALVIEW_CLCD_OFFSET_IMSC		(0x01c)
#define REALVIEW_CLCD_OFFSET_RIS		(0x020)
#define REALVIEW_CLCD_OFFSET_MIS		(0x024)
#define REALVIEW_CLCD_OFFSET_ICR		(0x028)
#define REALVIEW_CLCD_OFFSET_UCUR		(0x02c)
#define REALVIEW_CLCD_OFFSET_LCUR		(0x030)
#define REALVIEW_CLCD_OFFSET_PALETTE	(0x200)

#define REALVIEW_CNTL_LCDEN				(1 << 0)
#define REALVIEW_CNTL_LCDBPP1			(0 << 1)
#define REALVIEW_CNTL_LCDBPP2			(1 << 1)
#define REALVIEW_CNTL_LCDBPP4			(2 << 1)
