#ifndef __REALVIEW_REG_UART_H__
#define __REALVIEW_REG_UART_H__

#define REALVIEW_UART0_BASE				(0x10009000)
#define REALVIEW_UART1_BASE				(0x1000a000)
#define REALVIEW_UART2_BASE				(0x1000b000)
#define REALVIEW_UART3_BASE				(0x1000c000)

#define REALVIEW_UART_OFFSET_DATA		(0x00)
#define REALVIEW_UART_OFFSET_RSR		(0x04)
#define REALVIEW_UART_OFFSET_FR			(0x18)
#define REALVIEW_UART_OFFSET_IBRD		(0x24)
#define REALVIEW_UART_OFFSET_FBRD		(0x28)
#define REALVIEW_UART_OFFSET_LCRH		(0x2c)
#define REALVIEW_UART_OFFSET_CR			(0x30)
#define REALVIEW_UART_OFFSET_IMSC		(0x38)

#define REALVIEW_UART_RSR_OE			(0x08)
#define REALVIEW_UART_RSR_BE			(0x04)
#define REALVIEW_UART_RSR_PE			(0x02)
#define REALVIEW_UART_RSR_FE			(0x01)

#define REALVIEW_UART_FR_TXFE			(0x80)
#define REALVIEW_UART_FR_RXFF			(0x40)
#define REALVIEW_UART_FR_TXFF			(0x20)
#define REALVIEW_UART_FR_RXFE			(0x10)
#define REALVIEW_UART_FR_BUSY			(0x08)

#define REALVIEW_UART_LCRH_SPS			(1 << 7)
#define REALVIEW_UART_LCRH_WLEN_8		(3 << 5)
#define REALVIEW_UART_LCRH_WLEN_7		(2 << 5)
#define REALVIEW_UART_LCRH_WLEN_6		(1 << 5)
#define REALVIEW_UART_LCRH_WLEN_5		(0 << 5)
#define REALVIEW_UART_LCRH_FEN			(1 << 4)
#define REALVIEW_UART_LCRH_STP2			(1 << 3)
#define REALVIEW_UART_LCRH_EPS			(1 << 2)
#define REALVIEW_UART_LCRH_PEN			(1 << 1)
#define REALVIEW_UART_LCRH_BRK			(1 << 0)

#define REALVIEW_UART_CR_CTSEN			(1 << 15)
#define REALVIEW_UART_CR_RTSEN			(1 << 14)
#define REALVIEW_UART_CR_OUT2			(1 << 13)
#define REALVIEW_UART_CR_OUT1			(1 << 12)
#define REALVIEW_UART_CR_RTS			(1 << 11)
#define REALVIEW_UART_CR_DTR			(1 << 10)
#define REALVIEW_UART_CR_RXE			(1 << 9)
#define REALVIEW_UART_CR_TXE			(1 << 8)
#define REALVIEW_UART_CR_LPE			(1 << 7)
#define REALVIEW_UART_CR_IIRLP			(1 << 2)
#define REALVIEW_UART_CR_SIREN			(1 << 1)
#define REALVIEW_UART_CR_UARTEN			(1 << 0)

#define REALVIEW_UART_IMSC_OEIM			(1 << 10)
#define REALVIEW_UART_IMSC_BEIM			(1 << 9)
#define REALVIEW_UART_IMSC_PEIM			(1 << 8)
#define REALVIEW_UART_IMSC_FEIM			(1 << 7)
#define REALVIEW_UART_IMSC_RTIM			(1 << 6)
#define REALVIEW_UART_IMSC_TXIM			(1 << 5)
#define REALVIEW_UART_IMSC_RXIM			(1 << 4)
#define REALVIEW_UART_IMSC_DSRMIM		(1 << 3)
#define REALVIEW_UART_IMSC_DCDMIM		(1 << 2)
#define REALVIEW_UART_IMSC_CTSMIM		(1 << 1)
#define REALVIEW_UART_IMSC_RIMIM		(1 << 0)

#endif /* __REALVIEW_REG_UART_H__ */
