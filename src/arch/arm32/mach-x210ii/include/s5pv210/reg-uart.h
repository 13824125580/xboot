#ifndef __S5PV210_REG_UART_H__
#define __S5PV210_REG_UART_H__

#define S5PV210_UART0_BASE		(0xE2900000)
#define S5PV210_UART1_BASE		(0xE2900400)
#define S5PV210_UART2_BASE		(0xE2900800)
#define S5PV210_UART3_BASE		(0xE2900C00)

#define S5PV210_ULCON	 	 	(0x00)
#define S5PV210_UCON	 	 	(0x04)
#define S5PV210_UFCON	 	 	(0x08)
#define S5PV210_UMON	 	 	(0x0C)
#define S5PV210_UTRSTAT			(0x10)
#define S5PV210_UERSTAT			(0x14)
#define S5PV210_UFSTAT			(0x18)
#define S5PV210_UMSTAT			(0x1C)
#define S5PV210_UTXH			(0x20)
#define S5PV210_URXH			(0x24)
#define S5PV210_UBRDIV			(0x28)
#define S5PV210_UDIVSLOT		(0x2C)
#define S5PV210_UINTP			(0x30)
#define S5PV210_UINTSP			(0x34)
#define S5PV210_UINTM			(0x38)

#define S5PV210_UFSTAT_TXFULL	(1<<24)
#define S5PV210_UFSTAT_RXFULL	(1<<8)
#define S5PV210_UFSTAT_TXCOUNT	(0xFF<<16)
#define S5PV210_UFSTAT_RXCOUNT	(0xFF<<0)
#define S5PV210_UTRSTAT_TXE	  	(1<<2)
#define S5PV210_UTRSTAT_TXFE	(1<<1)
#define S5PV210_UTRSTAT_RXDR	(1<<0)
#define S5PV210_UERSTAT_OVERRUN	(1<<0)
#define S5PV210_UERSTAT_PARITY	(1<<1)
#define S5PV210_UERSTAT_FRAME	(1<<2)
#define S5PV210_UERSTAT_BREAK	(1<<3)
#define S5PV210_UMSTAT_CTS	  	(1<<0)
#define S5PV210_UMSTAT_DCTS		(1<<4)

#endif /* __S5PV210_REG_UART_H__ */
